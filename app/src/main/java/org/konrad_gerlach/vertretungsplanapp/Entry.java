public class Entry {
    String date ="";
    String caller ="";
    String type ="";
    String message="";
    public Entry(String date, String caller, String type, String message) {
        this.date = date;
        this.caller = caller;
        this.type = type;
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getCaller() {
        return caller;
    }

    public void setCaller(String caller) {
        this.caller = caller;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
